import pytest
from ShadowAnalyst.doctor_popup import parse_findings

@pytest.mark.parametrize("findings, expected_body, expected_alert", [
    (None, ["Патологий не обнаружено. Норма."], []),
    ("", ["Патологий не обнаружено. Норма."], []),
    ("Норма", ["Патологий не обнаружено. Норма."], []),
    ("норма", ["норма"], []),
    ("Зуб 11 в порядке\nЗуб 12: пломба, без патологий", ["Зуб 11 в порядке", "Зуб 12: пломба, без патологий"], []),
    ("Зуб 11: обнаружен КАРИЕС\nЗуб 12: кариес на контактной поверхности", [], ["Зуб 11: обнаружен КАРИЕС", "Зуб 12: кариес на контактной поверхности"]),
    ("Зуб 21: Воспаление десны", [], ["Зуб 21: Воспаление десны"]),
    ("Зуб 11: Норма\nЗуб 12: глубокий кариес\nЗуб 13: пломба\nЗуб 14: воспаление корня", ["Зуб 11: Норма", "Зуб 13: пломба"], ["Зуб 12: глубокий кариес", "Зуб 14: воспаление корня"])
])
def test_parse_findings_all_cases(findings, expected_body, expected_alert):
    body, alert = parse_findings(findings)
    assert body == expected_body
    assert alert == expected_alert
